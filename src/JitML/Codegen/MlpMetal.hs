-- | Sprint (backend-parity) — the Apple-Silicon (Metal lane) MLP
-- forward/backward kernel source, mirroring "JitML.Codegen.MlpCuda" as embedded
-- Metal Shading Language JIT-compiled at runtime via
-- @MTLDevice.makeLibrary(source:options:)@ (fast-math off), built through the
-- CommandLineTools @swift build@ — no offline @metal@ compiler, no Xcode, no VM
-- (see CLAUDE.md → Apple Silicon headless JIT). The emitted @\@_cdecl@ ABI is
-- identical to the CUDA / oneDNN backends (the @jitml_mlp_*@ symbols), so
-- "JitML.Numerics.MlpDevice" marshals to it unchanged.
--
-- ⚠ UNVERIFIED ON THIS HOST. This module emits Swift + MSL as text; it compiles
-- as Haskell, but the generated Metal program can only be compiled and executed
-- on an apple-silicon Mac. It is a faithful port of the verified CUDA kernels
-- ("JitML.Codegen.MlpCuda") — the MSL math mirrors the CUDA @__global__@
-- bodies and the launchers mirror the CUDA host wrappers — but it MUST be
-- validated on the @-p apple-silicon@ lane (which runs host-native on a Mac)
-- before it is trusted. The MSL kernels use per-thread sequential reductions
-- (no atomics) and single-stream launch order for the determinism contract.
module JitML.Codegen.MlpMetal
