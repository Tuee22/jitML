    , "  float h = hidden_act[i]; float d_pre = d_act * (1.0f - h * h);"
    , "  g_b1[i] = d_pre;"
    , "  for (int j = 0; j < inputs; ++j) { g_w1[i * inputs + j] = d_pre * input[j]; }"
