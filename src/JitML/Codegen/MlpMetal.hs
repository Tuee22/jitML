    , "  int i = int(gid); if (i >= hidden) { return; }"
    , "  float gb1 = 0.0f;"
    , "  for (int j = 0; j < inputs; ++j) { g_w1[i * inputs + j] = 0.0f; }"
    , "  for (int b = 0; b < batch; ++b) {"
    , "    float d_act = 0.0f;"
    , "    for (int k = 0; k < outputs; ++k) { d_act += w2[k * hidden + i] * d_l_dy[b * outputs + k]; }"
