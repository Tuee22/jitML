{-# LANGUAGE OverloadedStrings #-}

module JitML.Codegen.Cuda
  ( renderCudaSource
  , renderCudaFamilySource
  )
where

import Data.Text (Text)
import Data.Text qualified as Text

import JitML.Cache.Key (KernelSpec (..), Kind, TuningChoice (..), kindText)
import JitML.Codegen.KernelFamily (KernelFamily (..), familyName)
import JitML.Codegen.SourceFile (SourceFile (..))

renderCudaSource :: KernelSpec -> Kind -> TuningChoice -> [SourceFile]
renderCudaSource = renderCudaFamilySource Identity

renderCudaFamilySource
  :: KernelFamily -> KernelSpec -> Kind -> TuningChoice -> [SourceFile]
renderCudaFamilySource family kernelSpec kind tuningChoice =
  [ SourceFile "kernel.cu" (cudaSource family kernelSpec kind tuningChoice)
  ]

cudaSource :: KernelFamily -> KernelSpec -> Kind -> TuningChoice -> Text
cudaSource family kernelSpec kind tuningChoice =
  Text.unlines
    [ "// Generated by jitml. Do not check generated CUDA source into git."
    , "#include <cstddef>"
    , "#include <cuda_runtime.h>"
    , "#ifdef JITML_USE_CUDNN"
    , "#include <cudnn.h>"
    , "#endif"
    , "#ifdef JITML_USE_CUBLAS"
    , "#include <cublas_v2.h>"
    , "#endif"
    , ""
    , familyImpl family
    , ""
    , "static const char *jitml_kernel_spec = " <> cString (kernelSpecPayload kernelSpec) <> ";"
    , "static const char *jitml_kernel_kind = " <> cString (kindText kind) <> ";"
    , "static const char *jitml_tuning_choice = " <> cString (unTuningChoice tuningChoice) <> ";"
    , "static const char *jitml_kernel_family = " <> cString (familyName family) <> ";"
    , "static const char *jitml_cudnn_algorithm = " <> cString (cudnnDeterministicAlgorithm family) <> ";"
    , "static const char *jitml_rng_policy = \"host-splitmix64-no-curand\";"
    , "// determinism: --use_fast_math=false; TF32 disabled; warp-shuffle reductions"
    , ""
    , "extern \"C\" const char *jitml_kernel_family_name(void) { return jitml_kernel_family; }"
    , kernelOutputCountFunction family
    ]

familyImpl :: KernelFamily -> Text
familyImpl Identity =
  Text.unlines
    [ "extern \"C\" __global__ void jitml_kernel(float *out, const float *input, int n) {"
    , "  int i = blockIdx.x * blockDim.x + threadIdx.x;"
    , "  if (i < n) { out[i] = input[i]; }"
    , "}"
    , ""
    , "extern \"C\" void jitml_launch(float *out, const float *input, int n) {"
    , "  dim3 block(256);"
    , "  dim3 grid((n + block.x - 1) / block.x);"
    , "  jitml_kernel<<<grid, block>>>(out, input, n);"
    , "}"
    ]
familyImpl Reduction =
  Text.unlines
    [ "// Deterministic warp-shuffle reduction kernel. One partial is emitted per"
    , "// warp, and the host accumulates those partials in canonical index order."
    , "extern \"C\" __global__ void jitml_kernel(float *partials, const float *input, int n) {"
    , "  int tid = threadIdx.x;"
    , "  int warp = tid / warpSize;"
    , "  int idx = blockIdx.x * blockDim.x + tid;"
    , "  float v = idx < n ? input[idx] : 0.0f;"
    , "  for (int offset = warpSize / 2; offset > 0; offset >>= 1) {"
    , "    v += __shfl_down_sync(0xFFFFFFFFu, v, offset);"
    , "  }"
    , "  if ((tid & (warpSize - 1)) == 0) {"
    , "    int warpsPerBlock = blockDim.x / warpSize;"
    , "    partials[blockIdx.x * warpsPerBlock + warp] = v;"
    , "  }"
    , "}"
    , ""
    , "extern \"C\" void jitml_launch(float *partials, const float *input, int n) {"
    , "  dim3 block(256);"
    , "  dim3 grid((n + block.x - 1) / block.x);"
    , "  jitml_kernel<<<grid, block>>>(partials, input, n);"
    , "}"
    ]
familyImpl Dense2D =
