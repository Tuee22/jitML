familyImpl EmbeddingKernel = familyImpl Identity

cudnnDeterministicAlgorithm :: KernelFamily -> Text
cudnnDeterministicAlgorithm Conv2DKernel = "CUDNN_CONVOLUTION_FWD_ALGO_IMPLICIT_PRECOMP_GEMM"
cudnnDeterministicAlgorithm Conv3DKernel = "CUDNN_CONVOLUTION_FWD_ALGO_IMPLICIT_PRECOMP_GEMM"
cudnnDeterministicAlgorithm BatchNormKernel = "CUDNN_BATCHNORM_SPATIAL_PERSISTENT"
cudnnDeterministicAlgorithm LayerNormKernel = "CUDNN_BATCHNORM_SPATIAL_PERSISTENT"
cudnnDeterministicAlgorithm MultiHeadAttentionKernel = "deterministic-cublas-gemm"
cudnnDeterministicAlgorithm _ = "none"

cString :: Text -> Text
cString value =
  "\"" <> Text.concatMap escape value <> "\""

kernelOutputCountFunction :: KernelFamily -> Text
kernelOutputCountFunction Reduction =
  Text.unlines
    [ "extern \"C\" std::size_t jitml_kernel_output_count(std::size_t n) {"
    , "  const std::size_t block = 256;"
    , "  const std::size_t warp = 32;"
    , "  const std::size_t warpsPerBlock = block / warp;"
    , "  return ((n + block - 1) / block) * warpsPerBlock;"
    , "}"
    ]
kernelOutputCountFunction _ =
  "extern \"C\" std::size_t jitml_kernel_output_count(std::size_t n) { return n; }"

escape :: Char -> Text
escape '"' = "\\\""
escape '\\' = "\\\\"
escape '\n' = "\\n"
escape char = Text.singleton char
