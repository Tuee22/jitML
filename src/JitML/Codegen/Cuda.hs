familyImpl LayerNormKernel = familyImpl BatchNormKernel
familyImpl MultiHeadAttentionKernel =
  Text.unlines
    [ "// MHA scaffold. Real driver chains cublasSgemm for QKV linear and the output"
    , "// projection, plus deterministic softmax via warp-shuffle reductions."
    , "extern \"C\" __global__ void jitml_kernel(float *out, const float *input, int n) {"
    , "  int i = blockIdx.x * blockDim.x + threadIdx.x;"
    , "  if (i < n) { out[i] = input[i]; }"
