-- | The weighted-kernel ABI surfaces a second exported entry point
-- alongside `jitml_kernel`. Callers pass a flat row-major weights
-- buffer; for `Dense2D` the kernel runs a real GEMM
-- (`out = input · W`) against those weights. For other families this
-- pass through the unweighted body — the per-family weighted ABI for
-- Conv2D / Conv3D / BatchNorm / LayerNorm / MHA / Embedding lands
-- alongside Sprint 13.11's downstream extension and is tracked in the
-- sprint's Remaining Work.
weightedFamilyImpl :: KernelFamily -> Text
weightedFamilyImpl family =
  Text.unlines
    [ "// Sprint 13.11 — per-family weighted oneDNN primitive. The caller-"
    , "// supplied weights buffer drives the family's natural weight input"
    , "// (Dense2D matmul, Conv2D/3D filter, BatchNorm/LayerNorm affine,"
    , "// Embedding table, MHA QKV stack). Missing entries default to the"
    , "// family's canonical no-op value."
    , "extern \"C\" void jitml_weighted_kernel("
    , "    float *out, const float *input, std::size_t n,"
    , "    const float *weights, std::size_t weights_count) {"
    , "  " <> weightedFamilyCall family
    , "}"
    ]

-- | Maps each KernelFamily to the oneDNN helper call that consumes the
-- weighted ABI. Sprint 13.11 closure.
weightedFamilyCall :: KernelFamily -> Text
weightedFamilyCall Dense2D =
  "jitml_onednn_dense_weighted(out, input, n, weights, weights_count);"
weightedFamilyCall Conv2DKernel =
  "jitml_onednn_conv2d_weighted(out, input, n, weights, weights_count);"
weightedFamilyCall Conv3DKernel =
  "jitml_onednn_conv3d_weighted(out, input, n, weights, weights_count);"
weightedFamilyCall BatchNormKernel =
  "jitml_onednn_batchnorm_weighted(out, input, n, weights, weights_count);"
weightedFamilyCall LayerNormKernel =
  "jitml_onednn_layernorm_weighted(out, input, n, weights, weights_count);"
weightedFamilyCall MultiHeadAttentionKernel =
  "jitml_onednn_mha_weighted(out, input, n, weights, weights_count);"
weightedFamilyCall EmbeddingKernel =
  "jitml_onednn_embedding_weighted(out, input, n, weights, weights_count);"
weightedFamilyCall _ =
  "(void)weights; (void)weights_count; jitml_kernel(out, input, n);"

