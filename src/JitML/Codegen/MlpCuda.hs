forwardKernels :: Text
forwardKernels =
  Text.unlines
    [ "// hidden_pre[i] = b1[i] + sum_j W1[i*inputs + j] * input[j]"
    , "// hidden_act[i] = tanh(hidden_pre[i])   (one thread per hidden unit)"
    , "static __global__ void jitml_mlp_hidden_kernel("
    , "    float *hidden_pre, float *hidden_act,"
    , "    const float *input, const float *w1, const float *b1,"
    , "    int inputs, int hidden) {"
    , "  int i = blockIdx.x * blockDim.x + threadIdx.x;"
    , "  if (i >= hidden) { return; }"
    , "  float acc = b1[i];"
    , "  for (int j = 0; j < inputs; ++j) {"
    , "    acc += w1[i * inputs + j] * input[j];"
    , "  }"
    , "  hidden_pre[i] = acc;"
    , "  hidden_act[i] = tanhf(acc);"
    , "}"
    , ""
    , "// output[k] = b2[k] + sum_i W2[k*hidden + i] * hidden_act[i]"
    , "static __global__ void jitml_mlp_output_kernel("
    , "    float *output, const float *hidden_act,"
    , "    const float *w2, const float *b2,"
    , "    int hidden, int outputs) {"
    , "  int k = blockIdx.x * blockDim.x + threadIdx.x;"
    , "  if (k >= outputs) { return; }"
    , "  float acc = b2[k];"
    , "  for (int i = 0; i < hidden; ++i) {"
    , "    acc += w2[k * hidden + i] * hidden_act[i];"
    , "  }"
    , "  output[k] = acc;"
    , "}"
    ]

backwardKernels :: Text
backwardKernels =
  Text.unlines
    [ "// gB2[k] = dL/dy[k];  gW2[k*hidden + i] = dL/dy[k] * hidden_act[i]"
    , "static __global__ void jitml_mlp_grad_output_kernel("
    , "    float *g_w2, float *g_b2,"
    , "    const float *d_l_dy, const float *hidden_act,"
    , "    int hidden, int outputs) {"
    , "  int k = blockIdx.x * blockDim.x + threadIdx.x;"
    , "  if (k >= outputs) { return; }"
    , "  float dy = d_l_dy[k];"
    , "  g_b2[k] = dy;"
    , "  for (int i = 0; i < hidden; ++i) {"
    , "    g_w2[k * hidden + i] = dy * hidden_act[i];"
    , "  }"
    , "}"
    , ""
    , "// d_act[i] = sum_k W2[k*hidden + i] * dL/dy[k]"
    , "// d_pre[i] = d_act[i] * (1 - hidden_act[i]^2)"
    , "// gB1[i] = d_pre[i];  gW1[i*inputs + j] = d_pre[i] * input[j]"
    , "static __global__ void jitml_mlp_grad_hidden_kernel("
    , "    float *g_w1, float *g_b1,"
    , "    const float *d_l_dy, const float *input,"
    , "    const float *hidden_act, const float *w2,"
    , "    int inputs, int hidden, int outputs) {"
    , "  int i = blockIdx.x * blockDim.x + threadIdx.x;"
    , "  if (i >= hidden) { return; }"
    , "  float d_act = 0.0f;"
    , "  for (int k = 0; k < outputs; ++k) {"
    , "    d_act += w2[k * hidden + i] * d_l_dy[k];"
    , "  }"
    , "  float h = hidden_act[i];"
    , "  float d_pre = d_act * (1.0f - h * h);"
    , "  g_b1[i] = d_pre;"
    , "  for (int j = 0; j < inputs; ++j) {"
    , "    g_w1[i * inputs + j] = d_pre * input[j];"
    , "  }"
    , "}"
    ]

