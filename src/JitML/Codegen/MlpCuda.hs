-- | Sprint 13.8 — batched forward + summed-gradient backward device
-- kernels. The fused @jitml_mlp_batch_gradient@ wrapper (below) computes
-- one minibatch's gradient — summed over the @batch@ rows — in a single
-- device round-trip, amortising host↔device copies over the whole batch
-- (the trainer hot-path adoption these kernels enable). Layouts are
-- row-major: @input@ is @batch × inputs@, @d_l_dy@ is @batch × outputs@,
-- @hidden_act@ is @batch × hidden@. Each gradient element is reduced by a
-- single thread with sequential sums over the batch (no atomics), so the
-- summed gradient is bit-deterministic run-to-run on the same device.
batchedGradientKernels :: Text
batchedGradientKernels =
  Text.unlines
    [ "// hidden_act[b][i] = tanh(b1[i] + sum_j W1[i][j] * input[b][j])"
    , "static __global__ void jitml_mlp_batch_hidden_kernel("
    , "    float *hidden_act, const float *input, const float *w1, const float *b1,"
    , "    int inputs, int hidden, int batch) {"
    , "  std::size_t idx = static_cast<std::size_t>(blockIdx.x) * blockDim.x + threadIdx.x;"
    , "  std::size_t total = static_cast<std::size_t>(batch) * static_cast<std::size_t>(hidden);"
    , "  if (idx >= total) { return; }"
    , "  int b = static_cast<int>(idx / hidden);"
    , "  int i = static_cast<int>(idx % hidden);"
    , "  float acc = b1[i];"
    , "  for (int j = 0; j < inputs; ++j) {"
    , "    acc += w1[i * inputs + j] * input[b * inputs + j];"
    , "  }"
    , "  hidden_act[b * hidden + i] = tanhf(acc);"
    , "}"
    , ""
    , "// gB2[k] = sum_b dy[b][k];  gW2[k][i] = sum_b dy[b][k] * hidden_act[b][i]"
    , "static __global__ void jitml_mlp_batch_grad_output_kernel("
    , "    float *g_w2, float *g_b2, const float *d_l_dy, const float *hidden_act,"
    , "    int hidden, int outputs, int batch) {"
    , "  int k = blockIdx.x * blockDim.x + threadIdx.x;"
    , "  if (k >= outputs) { return; }"
    , "  float gb = 0.0f;"
    , "  for (int b = 0; b < batch; ++b) { gb += d_l_dy[b * outputs + k]; }"
    , "  g_b2[k] = gb;"
    , "  for (int i = 0; i < hidden; ++i) {"
    , "    float gw = 0.0f;"
    , "    for (int b = 0; b < batch; ++b) {"
    , "      gw += d_l_dy[b * outputs + k] * hidden_act[b * hidden + i];"
    , "    }"
    , "    g_w2[k * hidden + i] = gw;"
    , "  }"
    , "}"
    , ""
    , "// d_pre[b][i] = (sum_k W2[k][i]*dy[b][k]) * (1 - hidden_act[b][i]^2)"
    , "// gB1[i] = sum_b d_pre[b][i];  gW1[i][j] = sum_b d_pre[b][i] * input[b][j]"
    , "static __global__ void jitml_mlp_batch_grad_hidden_kernel("
    , "    float *g_w1, float *g_b1, const float *d_l_dy, const float *input,"
    , "    const float *hidden_act, const float *w2,"
    , "    int inputs, int hidden, int outputs, int batch) {"
    , "  int i = blockIdx.x * blockDim.x + threadIdx.x;"
    , "  if (i >= hidden) { return; }"
    , "  float gb1 = 0.0f;"
    , "  for (int j = 0; j < inputs; ++j) { g_w1[i * inputs + j] = 0.0f; }"
    , "  for (int b = 0; b < batch; ++b) {"
    , "    float d_act = 0.0f;"
    , "    for (int k = 0; k < outputs; ++k) {"
    , "      d_act += w2[k * hidden + i] * d_l_dy[b * outputs + k];"
    , "    }"
    , "    float h = hidden_act[b * hidden + i];"
    , "    float d_pre = d_act * (1.0f - h * h);"
    , "    gb1 += d_pre;"
    , "    for (int j = 0; j < inputs; ++j) {"
    , "      g_w1[i * inputs + j] += d_pre * input[b * inputs + j];"
    , "    }"
    , "  }"
    , "  g_b1[i] = gb1;"
    , "}"
    ]

-- | Batched output kernel: @output[b][k] = b2[k] + sum_i W2[k][i] *
-- hidden_act[b][i]@ (one thread per @(b, k)@). Pairs with
-- 'batchedGradientKernels'' batched hidden kernel to form the batched
-- forward pass.
batchedOutputKernel :: Text
batchedOutputKernel =
  Text.unlines
    [ "static __global__ void jitml_mlp_batch_output_kernel("
    , "    float *output, const float *hidden_act, const float *w2, const float *b2,"
    , "    int hidden, int outputs, int batch) {"
    , "  std::size_t idx = static_cast<std::size_t>(blockIdx.x) * blockDim.x + threadIdx.x;"
    , "  std::size_t total = static_cast<std::size_t>(batch) * static_cast<std::size_t>(outputs);"
    , "  if (idx >= total) { return; }"
    , "  int b = static_cast<int>(idx / outputs);"
    , "  int k = static_cast<int>(idx % outputs);"
    , "  float acc = b2[k];"
    , "  for (int i = 0; i < hidden; ++i) {"
    , "    acc += w2[k * hidden + i] * hidden_act[b * hidden + i];"
    , "  }"
    , "  output[b * outputs + k] = acc;"
    , "}"
    ]

