-- | Sprint 13.8 / 13.9 — nvcc-emitted forward/backward CUDA kernels for
-- the 'JitML.Numerics.Mlp' feed-forward network. This is the first real
-- step toward replacing the pure-Haskell backward pass with device
-- kernels (the single open item enumerated in Sprints 13.8 and 13.9
-- Remaining Work): the algorithmic contract — @y = W2 (tanh (W1 x + b1))
-- + b2@ and its manual reverse-mode gradient — is preserved, but the
-- arithmetic now runs on the GPU through generated CUDA.
--
-- The emitted @kernel.cu@ exposes two @extern \"C\"@ host wrappers that
-- own device allocation + host↔device copies:
--
--   * @jitml_mlp_forward@ — computes @hidden_pre@, @hidden_act = tanh
--     hidden_pre@, and @output = W2 hidden_act + b2@.
--   * @jitml_mlp_backward@ — given @dL/dy@, the forward @hidden_act@, the
--     input, and @W2@, computes the parameter gradients @gW1 / gB1 / gW2
--     / gB2@ exactly as 'JitML.Numerics.Mlp.mlpBackward'.
--
-- Each device thread accumulates its own reduction in a sequential loop
-- (no atomics, no warp-shuffle), so the result is bit-deterministic
-- run-to-run on the same device per the determinism contract. CUDA
-- single-precision arithmetic does not reproduce the host's @Double@
-- bit-for-bit; the backends test asserts agreement within a float
-- tolerance, and run-to-run device determinism exactly.
module JitML.Codegen.MlpCuda
  ( mlpCudaKernelSpec
  , renderMlpCudaSource
  )
where

