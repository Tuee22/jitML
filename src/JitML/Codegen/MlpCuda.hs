-- | Sprint 13.8 — batched device /input/-gradient kernels. The
-- @jitml_mlp_input_gradient_batch@ wrapper (below) computes @dL/dx@ for
-- every sample in a minibatch in one device round-trip:
-- @dL/dx = W1^T @ ((W2^T @ dL/dy) * (1 - tanh(hidden_pre)^2))@. Unlike the
-- parameter gradient (summed over the batch), the input gradient is
-- per-sample (each sample has its own @dL/dx@), so the output is
-- @batch × inputs@. This is the missing primitive for the continuous
-- actor-critic deterministic-policy gradient (@dQ/da@ = the action-slice
-- of the critic's input gradient). Per-thread sequential reductions →
-- bit-deterministic run-to-run on the same device.
batchedInputGradientKernels :: Text
batchedInputGradientKernels =
  Text.unlines
    [ "// d_pre[b][i] = (sum_k W2[k][i] * dL/dy[b][k]) * (1 - hidden_act[b][i]^2)"
    , "static __global__ void jitml_mlp_dhidden_pre_batch_kernel("
    , "    float *d_hidden_pre, const float *d_l_dy, const float *hidden_act,"
    , "    const float *w2, int hidden, int outputs, int batch) {"
    , "  std::size_t idx = static_cast<std::size_t>(blockIdx.x) * blockDim.x + threadIdx.x;"
    , "  std::size_t total = static_cast<std::size_t>(batch) * static_cast<std::size_t>(hidden);"
    , "  if (idx >= total) { return; }"
    , "  int b = static_cast<int>(idx / hidden);"
    , "  int i = static_cast<int>(idx % hidden);"
    , "  float d_act = 0.0f;"
    , "  for (int k = 0; k < outputs; ++k) {"
    , "    d_act += w2[k * hidden + i] * d_l_dy[b * outputs + k];"
    , "  }"
    , "  float h = hidden_act[b * hidden + i];"
    , "  d_hidden_pre[b * hidden + i] = d_act * (1.0f - h * h);"
    , "}"
    , ""
    , "// dx[b][j] = sum_i W1[i][j] * d_hidden_pre[b][i]"
    , "static __global__ void jitml_mlp_dx_batch_kernel("
    , "    float *dx, const float *d_hidden_pre, const float *w1,"
    , "    int inputs, int hidden, int batch) {"
    , "  std::size_t idx = static_cast<std::size_t>(blockIdx.x) * blockDim.x + threadIdx.x;"
    , "  std::size_t total = static_cast<std::size_t>(batch) * static_cast<std::size_t>(inputs);"
    , "  if (idx >= total) { return; }"
    , "  int b = static_cast<int>(idx / inputs);"
    , "  int j = static_cast<int>(idx % inputs);"
    , "  float acc = 0.0f;"
    , "  for (int i = 0; i < hidden; ++i) {"
    , "    acc += w1[i * inputs + j] * d_hidden_pre[b * hidden + i];"
    , "  }"
    , "  dx[b * inputs + j] = acc;"
    , "}"
    ]

